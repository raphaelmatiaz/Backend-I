
import multiprocessing

from time import sleep
import os

def clear_terminal():
    """Clears the terminal screen."""
    os.system('cls' if os.name == 'nt' else 'clear')

# Example usage:
clear_terminal()
print("This content will be printed after clearing the terminal.")

def animate(arrayOfFrames):
    currentFrameIndex = 0
    
    while True: 
        clear_terminal()
        
        currentFrame = arrayOfFrames[currentFrameIndex]

        print(currentFrame)
        
        
        currentFrameIndex += 1
        
        if currentFrameIndex >= len(arrayOfFrames):
            currentFrameIndex = 0  
        
        sleep(0.1)










